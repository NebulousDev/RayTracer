#include "math.h"
#include <math.h>

inline float32 sinf32(const float32 value)
{
    return sinf(value);
}

inline float32 cosf32(const float32 value)
{
    return cosf(value);
}

inline float32 tanf32(const float32 value)
{
    return tanf(value);
}

inline float32 asinf32(const float32 value)
{
    return asinf(value);
}

inline float32 acosf32(const float32 value)
{
    return acosf(value);
}

inline float32 atanf32(const float32 value)
{
    return atanf(value);
}

inline float32 sqrtf32(const float32 value)
{
    return sqrtf(value);
}